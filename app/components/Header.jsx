/**
 * @param {HeaderProps}
 */
export function Header({header, isLoggedIn, cart, publicStoreDomain}) {
  const {menu} = header;
  return (
    <header className="flex flex-col justify-center w-full text-primary">
      <div className="bg-primary text-white flex items-center justify-center p-2 tracking-wide font-thin scrolling-container">
        <span className="scrolling-text">
          Livraison gratuite, où que vous soyez! Livraison gratuite, où que vous
          soyez!
        </span>
      </div>
      <div className="flex gap-16 p-2 bg-white border-b-3">
        <HeaderMenu
          menu={menu}
          viewport="desktop"
          primaryDomainUrl={header.shop.primaryDomain.url}
          publicStoreDomain={publicStoreDomain}
        />
        <HeaderCtas isLoggedIn={isLoggedIn} cart={cart} />
      </div>
    </header>
  );
}

/**
 * @param {{
 *   menu: HeaderProps['header']['menu'];
 *   primaryDomainUrl: HeaderProps['header']['shop']['primaryDomain']['url'];
 *   viewport: Viewport;
 *   publicStoreDomain: HeaderProps['publicStoreDomain'];
 * }}
 */
export function HeaderMenu({
  menu,
  primaryDomainUrl,
  viewport,
  publicStoreDomain,
}) {
  const className = `header-menu-${viewport}`;
  const {close} = useAside();

  return (
    <nav className={className} role="navigation">
      {(menu || FALLBACK_HEADER_MENU).items.map((item) => {
        if (!item.url) return null;

        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain) ||
          item.url.includes(primaryDomainUrl)
            ? new URL(item.url).pathname
            : item.url;
        return (
          <NavLink
            end
            key={item.id}
            onClick={close}
            prefetch="intent"
            style={activeLinkStyle}
            to={url}
          >
            {item.title}
          </NavLink>
        );
      })}
      <NavLink to={'/partenaires'} style={activeLinkStyle} onClick={close}>
        Partenaires
      </NavLink>
      <NavLink to={'/contact'} style={activeLinkStyle} onClick={close}>
        Contact
      </NavLink>
      <NavLink to={'/faq'} style={activeLinkStyle} onClick={close}>
        FAQ
      </NavLink>
    </nav>
  );
}

/**
 * @param {Pick<HeaderProps, 'isLoggedIn' | 'cart'>}
 */
function HeaderCtas({isLoggedIn, cart}) {
  return (
    <nav className="w-full flex items-center justify-between" role="navigation">
      <div className="flex gap-4 items-center">
        <HeaderMenuMobileToggle />
        <NavLink prefetch="intent" to="/" end>
          <img src={logo} alt="Logo AuraBeats" />
        </NavLink>
      </div>
      <div className="flex gap-4 items-center justify-center">
        <NavLink to={'/wishlist'} style={activeLinkStyle}>
          <i>
            <FaStar size={'25px'} className="text-primary cursor-pointer" />
          </i>
        </NavLink>
        <SearchToggle />
        <CartToggle cart={cart} />
      </div>
    </nav>
  );
}

function HeaderMenuMobileToggle() {
  const {open} = useAside();
  return (
    <button
      className="header-menu-mobile-toggle reset"
      onClick={() => open('mobile')}
    >
      <i>
        <FiMenu size={'25px'} className="cursor-pointer" />
      </i>
    </button>
  );
}

function SearchToggle() {
  const {open} = useAside();
  return (
    <button className="reset" onClick={() => open('search')}>
      <IoSearch size={'25px'} className="cursor-pointer" />
    </button>
  );
}

/**
 * @param {{count: number | null}}
 */
function CartBadge({count}) {
  const {open} = useAside();
  const {publish, shop, cart, prevCart} = useAnalytics();

  return (
    <a
      href="/cart"
      onClick={(e) => {
        e.preventDefault();
        open('cart');
        publish('cart_viewed', {
          cart,
          prevCart,
          shop,
          url: window.location.href || '',
        });
      }}
    >
      <BsCartFill size={'25px'} className="cursor-pointer" />
    </a>
  );
}

/**
 * @param {Pick<HeaderProps, 'cart'>}
 */
function CartToggle({cart}) {
  return (
    <Suspense fallback={<CartBadge count={null} />}>
      <Await resolve={cart}>
        <CartBanner />
      </Await>
    </Suspense>
  );
}

function CartBanner() {
  const originalCart = useAsyncValue();
  const cart = useOptimisticCart(originalCart);
  return <CartBadge count={cart?.totalQuantity ?? 0} />;
}

